def celsius_para_fahrenheit(celsius: float) -> float:
    return (celsius * 9/5) + 32

def fahrenheit_para_celsius(fahrenheit: float) -> float:
    return (fahrenheit - 32) * 5/9

def metro_para_pes(metros: float) -> float:
    return metros * 3.28084

def pes_para_metro(pes: float) -> float:
    return pes / 3.28084

def quilometro_para_milhas(quilometros: float) -> float:
    return quilometros * 0.621371

def milhas_para_quilometro(milhas: float) -> float:
    return milhas / 0.621371

def dia_para_horas(dias: float) -> float:
    return dias * 24
